// --- 공용 유틸 ---
const shuffle = (arr) => {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
};

// 셔플백: 한 바퀴 돌기 전까지 중복 방지
import { useCallback, useRef } from "react";
export function useQuoteBag(source = QUOTES) {
  const bagRef = useRef({ bag: [], idx: 0 });

  const nextQuote = useCallback(() => {
    const state = bagRef.current;
    if (state.idx >= state.bag.length) {
      state.bag = shuffle(source);
      state.idx = 0;
    }
    return state.bag[state.idx++];
  }, [source]);

  return nextQuote;
}
